-- Print tambien imprime variables (ya vamos a eso)

nombre = "Max"
print(nombre)
-- Max





-- Como en Python usabamos '+' para concatenar strings, en Lua se usa '..'

print("Buenos dias "..nombre.." :)")
-- Buenos dias Max :)





-- Tambien podemos usar una ',' para separar los elementos con tabulaciones
print("Buenos dias",nombre,":)")
-- Buenos dias     Max     :)
