// Cuando creamos un archivo de Javascript, podemos exportar elementos de ese archivo para que puedan ser usados en otros archivos.

// Por ejemplo, podemos tener un archivo "matematicas.js" que tenga funciones para sumar, restar, multiplicar y dividir.
// Si queremos usar esas funciones en otro archivo, debemos exportarlas con "export".

// En cambio, si tenemos un archivo "main.js" que necesita usar las funciones que estan en "matematicas.js", debemos importar
// dicho archivo con "import".

// Los exports no solo pueden ser funciones, tambien pueden ser variables, constantes, clases, etc.




// Hay 2 formas de exportar elementos de un archivo:
// 1. Exportacion nombrada
// 2. Exportacion por defecto




