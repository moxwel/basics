var nombre = "Max"
var color = "morado"

// Un string puede ser formateado de diferentes formas.

// Concatenando strings
var mensaje1 = "Hola soy " + nombre + " y mi color favorito es " + color + ".";

// Usando placeholders
var mensaje2 = `Hola soy ${nombre} y mi color favorito es ${color}.`;

console.log(mensaje1) // Hola soy Max y mi color favorito es morado.

console.log(mensaje2) // Hola soy Max y mi color favorito es morado.
