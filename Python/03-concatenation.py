# Podemos unir (concatenar) cadenas, sumandolas.
print("Hola" + " mundo") # Hola mundo

# Tambien podemos usar variables de tipo string.
nombre = "Max"
print("Hola " + nombre) # Hola max

# No podemos unir un string con un numero. En ese caso, tenemos que transformar el numero en un string con la funcion str().
print("El numero es " + str(5)) # El numero es 5

# Tambien podemos transformar cadenas de texto en numeros con la funcion int() o float().
print(5 + int("5")) # 10

# Podemos repetir cadenas de texto multiplicandolos.
print("Hola " * 5) # Hola Hola Hola Hola Hola