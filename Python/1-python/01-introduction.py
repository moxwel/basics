# Para comenzar a usar Python veamos primero como poder imprimir cosas en pantalla. Para eso, podemos usar la funcion print() . Para usar cadenas de texto, hay que usar doble comillas (""), todo lo que este dentro de las comillas, Python lo tratara como texto normal.
print("Hola mundo")

# Tambien sirve usanso comillas simples.
print('Hola mundo')

# Para saltos de linea, podemos usar "\n".
print("\nSpam\nand\nEggs...\n")
# Spam
# and
# Eggs

# Podemos hacer calculos matematicos simples.
print(1+3+4) # 8
print(10-9) # 1
print(4*5) # 20

# Dividir con una sola barra, generara un numero flotante (o decimal).
print(10/2) # 5.0
print(5/2) # 2.5

# Dividir con dos barras generara un numero entero.
print(10//2) # 5
print(5//2) # 2 (se trunca el 2.5)

# Para elevar numeros, podemos usar dos asteriscos.
print(3**3) # 27
print(9**(1/2)) # 3.0

# Para calcular el modulo de una division (o resto), se usa el simbolo de porcentaje.
print(10%4) # 2