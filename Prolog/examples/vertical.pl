% Para que un segmento sea vertical, este debe
% tener dos puntos, donde la otra solamente cambie la coordenada Y
vertical(seg(punto(X, Y1), punto(X, Y2))).

% Para que un segmento sea horizontal, este debe
% tener dos puntos, donde la otra solamente cambie la coordenada X
horizontal(seg(punto(X1, Y), punto(X2, Y))).